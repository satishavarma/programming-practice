// POINT 1 = Store Number
// the variable positiveNumber stores the number 1234.
const positiveNumber = 1234;
console.log(positiveNumber);


// Negative number
// Numbers can also be negative. In the example given below, the variable negativeNumber stores the number -1234 which is a negative value.

const negativeNumber = -1234;
console.log(negativeNumber);


//  Fractional number
// In the example given below, the variable fractionalNumber stores the number 12.34.

const fractionalNumber = 12.34;
console.log(fractionalNumber);


// POINT 2 = NaN
// In JavaScript, NaN stands for Not a Number. If we perform an arithmetic operation on a value that is not a number, we get NaN as the result.

const personID = "123abc";
const product = personID * 2;
console.log(product);


// POINT 3 =Increase or Decrease a Number by One

let number = 10;
number++; // This is same as number = number + 1

console.log(number);


// We can use -- to decrement the value of a number by 1.
let numberNegative = 10;
number--; // This is same as number = number - 1

console.log(numberNegative);


// POINT 4 = Get the Remainder
// We can use % to get the remainder. It returns the remainder after the division of the value on the left of % by the value on the right of %.

console.log(5 % 3); 