// POINT 1 = Store Number
// the variable positiveNumber stores the number 1234.
const positiveNumber = 1234;
console.log(positiveNumber);


// Negative number
// Numbers can also be negative. In the example given below, the variable negativeNumber stores the number -1234 which is a negative value.

const negativeNumber = -1234;
console.log(negativeNumber);


//  Fractional number
// In the example given below, the variable fractionalNumber stores the number 12.34.

const fractionalNumber = 12.34;
console.log(fractionalNumber);